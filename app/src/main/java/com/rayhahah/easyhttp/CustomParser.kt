class CustomParser : Parser {
    /**
     * 数据解析
     */
    override fun parse(response: Response): Any? {
        return response.body()?.string()
    }

    /**
     * 数据解析的规则
     */
    override fun isCanParse(response: Response): Boolean {
        return true
    }

    /**
     * 数据解析
     */
    override fun unParse(response: Response): Any? {
        return response.body()?.string()
    }
}