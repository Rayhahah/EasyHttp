class Params {

    companion object {
        const val SINGLE_FILE = "SIMGLE_FILE"
        const val LIST_FILE = "LIST_FILE"
        const val PARAMS_MAP = "PARAMS_MAP"

    }


    var data = HashMap<String, String>()
    var files = Files()
    var file: Files.() -> Unit by LambdaDelegate<Files>(files)

     operator public fun String.invoke(value: String) {
        data[this] = value
    }


    fun getType(): String {
        if (files.DEFAULT != null) {
            return SINGLE_FILE
        } else if (files.data.size < 1) {
            return PARAMS_MAP
        } else if (files.data.size >= 1) {
            return LIST_FILE
        } else {
            return PARAMS_MAP
        }
    }
}