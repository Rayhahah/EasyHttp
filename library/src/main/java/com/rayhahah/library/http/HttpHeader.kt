class HttpHeader(var data: HashMap<String, String>) : Map<String, String> by data {

    var ACCEPT by MapDelegate(data, "Accept")
    var PRAGMA by MapDelegate(data, "Pragma")
    var PROXY_CONNECTION by MapDelegate(data, "Proxy-Connection")
    var USER_AGENT by MapDelegate(data, "User-Agent")
    var ACCEPT_ENCODING by MapDelegate(data, "accept-encoding")
    var CACHE_CONTROL by MapDelegate(data, "Cache-Control")
    var CONTENT_ENCODING by MapDelegate(data, "Content-Encoding")
    var CONNECTION by MapDelegate(data, "Connection")
    var CONTENT_LENGTH by MapDelegate(data, "Content-length")

   operator fun String.invoke(value: String) {
        data[this] = value
    }
}