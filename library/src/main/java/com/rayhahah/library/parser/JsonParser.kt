class JsonParser<T> : Parser<T> {

    private val gson = Gson()

    private val CONTENT_TYPE = "application/json;charset=UTF-8"
    override fun parse(response: Response, type: Type): T {
        val reader = InputStreamReader(response.body()?.byteStream())
        return gson.fromJson(reader, type)
    }

    override fun isCanParse(contentType: String, type: Type): Boolean {
        return CONTENT_TYPE == contentType && type::class.java.name != "okhttp3.Response"

    }

    override fun parse(content: String, type: Type): T {
        return gson.fromJson(content, type)
    }
}