class JsonParser<T>(val type: Type, private val className: String) : Parser {

    companion object {
        const val NEED_PARSE = "NEED_PARSE"
        const val STRING_NAME = "java.lang.String"
        const val RESPONSE_BODY_NAME = "okhttp3.ResponseBody"
        val FILTER_STR = arrayOf(STRING_NAME, RESPONSE_BODY_NAME)
    }

    private val gson = Gson()

    private val CONTENT_TYPE = "application/json;charset=UTF-8"
    override fun parse(response: Response): T {
        val reader = InputStreamReader(response.body()?.byteStream())
        return gson.fromJson(reader, type)
    }

    override fun isCanParse(response: Response): Boolean {
        return CONTENT_TYPE == response.body()?.contentType().toString() && isNeedParse() == NEED_PARSE
    }

    fun isNeedParse(): String {
        for (str in FILTER_STR) {
            if (str == className) {
                return str
            }
        }
        return NEED_PARSE
    }

    override fun unParse(response: Response): T {
        return when (isNeedParse()) {
            STRING_NAME -> return response.body()?.string() as T
            RESPONSE_BODY_NAME -> return response.body() as T
            else -> throw Exception("Can not be parsed!!!")
        }
    }
}